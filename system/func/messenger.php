function getnewmessages($userID)
{
    return mysqli_num_rows(
        safe_query(
            "SELECT
                messageID
            FROM
                `" . PREFIX . "plugins_messenger`
            WHERE
                `touser` = " . (int)$userID . " AND
                `userID` = " . (int)$userID . " AND
                `viewed` = 0"
        )
    );
}

